package com.spxts.tdms.model;


public class SessionTestConditions {
     private String weather;
     private String internalTemp;
     private String airTemp;
     private String humidity;
     private String haveOverallCf;
     private String overallCf;
	public String getWeather() {
		return weather;
	}
	public void setWeather(String weather) {
		this.weather = weather;
	}
	public String getInternalTemp() {
		return internalTemp;
	}
	public void setInternalTemp(String internalTemp) {
		this.internalTemp = internalTemp;
	}
	public String getAirTemp() {
		return airTemp;
	}
	public void setAirTemp(String airTemp) {
		this.airTemp = airTemp;
	}
	public String getHumidity() {
		return humidity;
	}
	public void setHumidity(String humidity) {
		this.humidity = humidity;
	}
	public String getHaveOverallCf() {
		return haveOverallCf;
	}
	public void setHaveOverallCf(String haveOverallCf) {
		this.haveOverallCf = haveOverallCf;
	}
	public String getOverallCf() {
		return overallCf;
	}
	public void setOverallCf(String overallCf) {
		this.overallCf = overallCf;
	}     
}
