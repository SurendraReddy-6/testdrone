package com.spxts.tdms.model;

public class SessionsBushing {
	private  String id;
	  
    private  String serialNumber;
    
    private  String designation;
  
    private  String terminationPhase;
  
    private  String status;
	
	public String getId() {
	return id;
	}
	
	public void setId(String id) {
	this.id = id;
	}
	
	public String getSerialNumber() {
	return serialNumber;
	}
	
	public void setSerialNumber(String serialNumber) {
	this.serialNumber = serialNumber;
	}
	
	public String getDesignation() {
	return designation;
	}
	
	public void setDesignation(String designation) {
	this.designation = designation;
	}
	
	public String getTerminationPhase() {
	return terminationPhase;
	}
	
	public void setTerminationPhase(String terminationPhase) {
	this.terminationPhase = terminationPhase;
	}
	
	public String getStatus() {
	return status;
	}
	
	public void setStatus(String status) {
	this.status = status;
	}

}
